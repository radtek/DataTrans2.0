package com.hisign.datatrans.domain;

import javax.sql.DataSource;

/**
 * 数据库连接信息
 * 
 * @author Hong
 */
public class DataSourceInfo {

	private String id;

	private String unitCode;

	private String unitName;

	private String dbUrl;

	private String dbUserName;

	private String dbPassword;

	private String openFlag;

	private boolean report;

	private String ifConnect;

	private int dataReportCount;

	private DataSource dataSource;

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getUnitCode() {
		return unitCode;
	}

	public void setUnitCode(String unitCode) {
		this.unitCode = unitCode;
	}

	public String getUnitName() {
		return unitName;
	}

	public void setUnitName(String unitName) {
		this.unitName = unitName;
	}

	public String getDbUrl() {
		return dbUrl;
	}

	public void setDbUrl(String dbUrl) {
		this.dbUrl = dbUrl;
	}

	public String getDbUserName() {
		return dbUserName;
	}

	public void setDbUserName(String dbUserName) {
		this.dbUserName = dbUserName;
	}

	public String getDbPassword() {
		return dbPassword;
	}

	public void setDbPassword(String dbPassword) {
		this.dbPassword = dbPassword;
	}

	public String getOpenFlag() {
		return openFlag;
	}

	public void setOpenFlag(String openFlag) {
		this.openFlag = openFlag;
	}

	public String getIfConnect() {
		return ifConnect;
	}

	public void setIfConnect(String ifConnect) {
		this.ifConnect = ifConnect;
	}

	public int getDataReportCount() {
		return dataReportCount;
	}

	public void setDataReportCount(int dataReportCount) {
		this.dataReportCount = dataReportCount;
	}

	public DataSource getDataSource() {
		return dataSource;
	}

	public void setDataSource(DataSource dataSource) {
		this.dataSource = dataSource;
	}

	public boolean isReport() {
		return report;
	}

	public void setReport(boolean report) {
		this.report = report;
	}

}
